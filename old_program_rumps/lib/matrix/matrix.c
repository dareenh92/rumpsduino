void Write_MX_byte(u8 DATA)
{
  u8 i;

  GPIO_BCLR = MX_CS;

  for(i=8;i>=1;i--)
  {
  GPIO_BCLR = MX_CLK;

  if (DATA&0x80)
    GPIO_BSET = MX_DIN;
  else
    GPIO_BCLR = MX_DIN;

    DATA=DATA<<1;

    GPIO_BSET = MX_CLK;
  }
}


//-------------------------------------------
//
void Write_MX(u8 address, u8 dat)
{
  GPIO_BCLR = MX_CS;

  Write_MX_byte(address);           //
  Write_MX_byte(dat);               //

  GPIO_BSET = MX_CS;
}


//------------------------------------------
//
void Init_MX(void)
{
  //GPIO_OEN_SET = 0x70;	// set matrix control output
  GPIO_OEN_SET = MX_CLK | MX_CS | MX_DIN;	// set matrix control output

  //MUXC_PU &= 0x8f;	// turn off PU for matrix control output
  MUXC_PU &= ~(MX_CLK | MX_CS | MX_DIN);

  Write_MX(0x09, 0x00);       //
  Write_MX(0x0a, 0x03);       //
  Write_MX(0x0b, 0x07);       //
  Write_MX(0x0c, 0x01);       //
  Write_MX(0x0f, 0x00);       //
}
